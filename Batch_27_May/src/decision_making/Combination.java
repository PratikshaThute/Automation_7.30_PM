package decision_making;

public class Combination {

	public static void main(String[] args) {
		
		int month_no = 1;
		String month =null;
		
		switch(month_no){
		case 1:
			month = "Jan";
			System.out.println(month);
			break;
		case 2:
			month = "Feb";
			System.out.println(month);
			break;
		case 3:
			month = "march";
			System.out.println(month);
			break;
		case 4:
			month = "april";
			System.out.println(month);
			break;
		case 5:
			month = "May";
			System.out.println(month);
			break;
		case 6:
			month = "June";
			System.out.println(month);
			break;
		case 7:
			month = "July";
			System.out.println(month);
			break;
		case 8:
			month = "aug";
			System.out.println(month);
			break;
			
		case 9:
			month = "september";
			System.out.println(month);
			break;
		case 10:
			month = "oct";
			System.out.println(month);
			break;
		case 11:
			month = "nov";
			System.out.println(month);
			break;
		case 12:
			month = "Dec";
			System.out.println(month);
			break;
			
			default:
				System.out.println("invalid month no");
			
		}
		
		System.out.println("the month is : " + month);
	
	}

}
