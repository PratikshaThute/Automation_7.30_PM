package decision_making;

public class If_elseif {

	public static void main(String[] args) {
		
		int p = 500;
		int q = 700;
		
		if(p==q) {
			System.out.println("p equals to q");
		}else if(p==q) {
			System.out.println("p is less than q");
		}else if(p==q) {
			System.out.println("p is less than equal to q");
		}else if(p==q) {
			System.out.println("p is greater than q");
		}else if(p>q) {
			System.out.println("p is greater than equal to q");
		}else {
			System.out.println("none of the above");
		}

	}

}
