package decision_making;

public class String_switch {

	public static void main(String[] args) {

		String name = "pratiksha";

		switch (name) {

		case "pratigya":
			System.out.println("surname -qwertyu ");
			break;

		case "pratiksha":
			System.out.println("surname - Thute");
			break;

		case "pranali":
			System.out.println("surname -xcvbnm");
			break;

		case "preksha":
			System.out.println("surname -ertyu");
			break;

		default:
			System.out.println("all the above cases are false");

		}

	}

}
